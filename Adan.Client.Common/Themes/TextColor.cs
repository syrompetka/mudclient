namespace Adan.Client.Common.Themes
{
    /// <summary>
    /// Enumeration of supported text colors.
    /// </summary>
    public enum TextColor
    {
        /// <summary>
        /// Gray color.
        /// </summary>
        Grey,

        /// <summary>
        /// Charcoal color.
        /// </summary>
        Charcoal,

        /// <summary>
        /// Brown color.
        /// </summary>
        Brown,

        /// <summary>
        /// White color.
        /// </summary>
        White,

        /// <summary>
        /// Bright white color.
        /// </summary>
        BrightWhite,

        /// <summary>
        /// Black color.
        /// </summary>
        Black,

        /// <summary>
        /// Bright black color.
        /// </summary>
        BrightBlack,

        /// <summary>
        /// Red color.
        /// </summary>
        Red,

        /// <summary>
        /// Bright red color.
        /// </summary>
        BrightRed,

        /// <summary>
        /// Green color.
        /// </summary>
        Green,

        /// <summary>
        /// Bright green color.
        /// </summary>
        BrightGreen,

        /// <summary>
        /// Yellow color.
        /// </summary>
        Yellow,

        /// <summary>
        /// Bright yellow color.
        /// </summary>
        BrightYellow,

        /// <summary>
        /// Blue color.
        /// </summary>
        Blue,

        /// <summary>
        /// Bright blue color.
        /// </summary>
        BrightBlue,

        /// <summary>
        /// Magenta color.
        /// </summary>
        Magenta,

        /// <summary>
        /// Bright magenta color.
        /// </summary>
        BrightMagenta,

        /// <summary>
        /// Cyan color.
        /// </summary>
        Cyan,

        /// <summary>
        /// Bright cyan color.
        /// </summary>
        BrightCyan,

        /// <summary>
        /// There is no color.
        /// </summary>
        None,

        /// <summary>
        /// A color of repeated command text.
        /// </summary>
        RepeatCommandTextColor
    }
}
